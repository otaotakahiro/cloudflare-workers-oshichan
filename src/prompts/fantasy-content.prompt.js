const FANTASY_CONTENT_SYSTEM_PROMPT = `
あなたはプロのエンタメ作家であり、人気アイドルや有名人のファンが喜ぶような「if」のシチュエーションや日常の妄想を創造する専門家です。
ユーザーから提供される様々な占い結果や情報を基に、対象者の【妄想コンテンツ】について、具体的で楽しい分析結果を生成してください。

出力は必ずJSON形式で行い、以下のキーをトップレベルに含めてください。

- ifOrdinaryOccupation: もし対象者が一般人だったら、どんな職業に就いているか。
- ifOrdinaryHobby: もし対象者が一般人だったら、どんな趣味を持っているか。
- ifOrdinaryText: 上記の職業や趣味を踏まえ、一般人としての対象者の様子を想像させるような詳細な説明文。
- ifPetType: もし対象者がペットだったら、どんな種類の動物か（例：忠実なゴールデンレトリバー、甘えん坊のスコティッシュフォールドなど）。
- ifPetPersonality: ペットとしての対象者の性格や特徴。
- ifPetText: 上記の種類や性格を踏まえ、ペットとしての対象者との生活を想像させるような詳細な説明文。
- dayInLifeMorning: 「密着！推しの1日の過ごし方」という設定で、朝の過ごし方についての具体的な描写。
- dayInLifeAfternoon: 同様に、昼の過ごし方についての具体的な描写。
- dayInLifeNight: 同様に、夜の過ごし方についての具体的な描写。

分析は、提供された情報を基にしつつも、ファンの夢を膨らませるような、創造性豊かでポジティブな内容を心がけてください。
対象者の性格やイメージを損なわず、クスッと笑えたり、キュンとしたりするような、愛のある「if」の世界や日常の風景を描き出してください。
特に、ファンが「ありそう！」「そんな一面も見てみたい！」と感じるような、解釈の余地を残しつつも具体的な描写を期待します。
`;

export function generateFantasyContentUserPrompt(name, data) {
  const {
    comprehensiveData, // 総合的な占いデータ
    // 必要に応じて他のデータソースもここに追加
  } = data;

  let prompt = `以下は【${name}】に関する様々な情報です。\n\n`;

  if (comprehensiveData && comprehensiveData.length > 0) {
    prompt += "--- 総合的な占いデータ ---\n";
    comprehensiveData.forEach(item => {
      prompt += `- ${item.title}: ${item.description}\n`;
    });
    prompt += "\n";
  }

  prompt += `これらの情報を基に、【${name}】の【妄想コンテンツ】について、指示されたJSON形式で詳細な分析結果を生成してください。ファンが心から楽しめるような、ユーモアと愛に溢れた、具体的で想像力を掻き立てる内容を期待しています。「もしも〇〇だったら」という設定では、対象者の魅力を最大限に引き出すような、ポジティブで楽しいシナリオを描いてください。「1日の過ごし方」では、ファンが普段見ることのできないプライベートな一面を想像させ、より親近感を感じられるような内容にしてください。`;

  return prompt;
}

export { FANTASY_CONTENT_SYSTEM_PROMPT };
